/**
 * 合并所有牌的含义
 */
export const ALL_TAROT_MEANINGS: Map<number, TarotCardMeaning> = new Map([
  ...MAJOR_ARCANA_MEANINGS.map(m => [m.id, m] as [number, TarotCardMeaning]),
  ...WANDS_MEANINGS.map(m => [m.id, m] as [number, TarotCardMeaning]),
  ...CUPS_MEANINGS.map(m => [m.id, m] as [number, TarotCardMeaning]),
  ...SWORDS_MEANINGS.map(m => [m.id, m] as [number, TarotCardMeaning]),
  ...PENTACLES_MEANINGS.map(m => [m.id, m] as [number, TarotCardMeaning]),
]);

/**
 * 获取牌的详细含义
 */
export function getCardMeaning(cardId: number): TarotCardMeaning | undefined {
  return ALL_TAROT_MEANINGS.get(cardId);
}

/**
 * 获取牌的当前含义（根据正逆位）
 */
export function getCurrentMeaning(cardId: number, isReversed: boolean): string {
  const meaning = getCardMeaning(cardId);
  if (!meaning) return '暂无详细解读';
  return isReversed ? meaning.reversed : meaning.upright;
}

/**
 * 获取牌的关键词
 */
export function getKeywords(cardId: number): string[] {
  const meaning = getCardMeaning(cardId);
  return meaning?.keywords || [];
}
