/// 根据宫位类型获取关键词
pub fn get_keyword_by_gong_wei(gong_wei: crate::types::GongWei, index: u8) -> &'static str {
    use crate::types::GongWei;

    match gong_wei {
        GongWei::MingGong => get_ming_gong_keyword(index),
        GongWei::CaiBo => get_cai_bo_keyword(index),
        GongWei::GuanLu => get_guan_lu_keyword(index),
        GongWei::FuQi => get_fu_qi_keyword(index),
        GongWei::JiE => get_ji_e_keyword(index),
        GongWei::FuDe => get_fu_de_keyword(index),
        _ => "未知",
    }
}

